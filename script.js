const holeGroup =
document.getElementById("holeGroup");

let circles = [];

function createDots(){

    holeGroup.innerHTML = "";
    circles = [];

    const step = 122;

    const cols =
    Math.ceil(window.innerWidth / step);

    const rows =
    Math.ceil(window.innerHeight / step) + 4;

    const startX = 98;
    const startY = 20;

    for(let y=0;y<rows;y++){

        for(let x=0;x<cols;x++){

            const circle =
            document.createElementNS(
                "http://www.w3.org/2000/svg",
                "circle"
            );

            circle.setAttribute(
                "cx",
                startX + x*step
            );

            circle.setAttribute(
                "cy",
                startY + y*step
            );

            circle.setAttribute(
                "r",
                48
            );

            circle.dataset.active="1";

            holeGroup.appendChild(circle);

            circles.push(circle);

        }

    }

}

createDots();

window.addEventListener("resize",createDots);


let mouseX = window.innerWidth / 2;
let mouseY = window.innerHeight / 2;

window.addEventListener("mousemove", (e) => {
  mouseX = e.clientX;
  mouseY = e.clientY;
});

setInterval(() => {

  circles.forEach(circle => {

    circle.dataset.active =
      Math.random() > 0.5 ? "1" : "0";

  });

}, 1500);

function update() {

  circles.forEach(circle => {

    const cx = parseFloat(circle.getAttribute("cx"));
    const cy = parseFloat(circle.getAttribute("cy"));

    const dx = cx - mouseX;
    const dy = cy - mouseY;
    const dist = Math.sqrt(dx * dx + dy * dy);

    if (dist < 120) {

      circle.style.visibility = "hidden";

    } else {

      circle.style.visibility =
        circle.dataset.active === "1"
          ? "visible"
          : "hidden";

    }

  });

  requestAnimationFrame(update);
}

update();


